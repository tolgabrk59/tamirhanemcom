// PID'i kod ile bul
export function getPIDByCode(code: string): LiveDataPID | undefined {
  return liveDataPIDs.find((pid) => pid.pidCode === code || pid.pidCode.toLowerCase() === code.toLowerCase());
}

// Kategoriye göre PID'leri getir
export function getPIDsByCategory(category: string): LiveDataPID[] {
  return liveDataPIDs.filter((pid) => pid.category === category);
}

// Tüm PID slug'larını getir (statik sayfa üretimi için)
export function getAllPIDSlugs(): { pid: string }[] {
  return liveDataPIDs.map((pid) => ({
    pid: pid.pidCode.toLowerCase(),
  }));
}
